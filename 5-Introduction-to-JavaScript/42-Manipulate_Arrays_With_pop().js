/*
Use the pop() array method to "pop" a value off the end of an array and optionally
store it in a variable; .pop() removes the last element from an array and returns it.
*/

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

// Setup
var myArray = [["John", 23], ["cat", 2]];

